# config.py — Все настройки бота в одном месте
# ============================================
# Меняй значения здесь, а не в коде бота.

# ── Binance подключение ──────────────────────
# Для paper trading API-ключи НЕ нужны.
# Бот читает только публичные данные (свечи, сделки).
# Когда будешь готов торговать реально — впишешь ключи сюда.
EXCHANGE_ID = "binanceusdm"          # Binance USDT-M Futures
API_KEY = ""                          # пока пусто — paper trading
API_SECRET = ""                       # пока пусто — paper trading

# ── Watchlist (какие монеты торгуем) ─────────
# Начинаем с 5 самых ликвидных пар.
# Потом можешь добавить/убрать.
WATCHLIST = [
    "BTC/USDT:USDT",
    "ETH/USDT:USDT",
    "SOL/USDT:USDT",
    "XRP/USDT:USDT",
    "DOGE/USDT:USDT",
]

# ── Таймфреймы ───────────────────────────────
# Бот смотрит на несколько таймфреймов одновременно:
# - Старший (1h) — для определения ключевых уровней
# - Средний (15m) — для подтверждения
# - Рабочий (5m) — для поиска входа
# - Точный (1m) — для точного входа (скальпинг)
TIMEFRAMES = ["1h", "15m", "5m", "1m"]
PRIMARY_TF = "5m"                     # основной рабочий таймфрейм

# ── Определение уровней ──────────────────────
# LEVEL_WINDOW: сколько свечей в каждую сторону смотрим
#   для поиска локального максимума/минимума.
#   Больше = меньше уровней, но они сильнее.
#   Для 5m: 10 значит смотрим 50 минут в каждую сторону.
LEVEL_WINDOW = 10

# CLUSTER_ATR_MULT: уровни ближе чем ATR * этот множитель
#   объединяются в один. ATR = средний размах свечи.
#   0.5 = уровни в пределах половины ATR считаются одним.
CLUSTER_ATR_MULT = 0.5

# MIN_TOUCHES: сколько раз цена должна коснуться уровня,
#   чтобы он считался значимым. 2 = минимум.
MIN_TOUCHES = 2

# ATR_PERIOD: за сколько свечей считать ATR (волатильность).
ATR_PERIOD = 14

# ── Анализ объёма ────────────────────────────
# VOLUME_AVG_PERIOD: за сколько свечей считать средний объём.
VOLUME_AVG_PERIOD = 20

# VOLUME_SPIKE_MULT: во сколько раз объём должен превысить
#   среднее, чтобы считаться всплеском. 1.5 = в полтора раза.
VOLUME_SPIKE_MULT = 1.5

# ── Сигналы ──────────────────────────────────
# NEAR_LEVEL_ATR_MULT: расстояние до уровня (в ATR),
#   при котором считаем что "цена у уровня".
#   0.3 = 30% от ATR.
NEAR_LEVEL_ATR_MULT = 0.3

# BREAKOUT_CONFIRM_CANDLES: сколько свечей цена должна
#   продержаться за уровнем для подтверждения пробоя.
BREAKOUT_CONFIRM_CANDLES = 2

# MIN_RISK_REWARD: минимальное соотношение прибыль/риск.
#   2.0 = потенциальная прибыль минимум в 2 раза больше риска.
MIN_RISK_REWARD = 2.0

# ── Управление позицией ──────────────────────
# RISK_PER_TRADE: какой % от депозита рискуем в одной сделке.
#   0.01 = 1%. На $1000 депозита рискуем $10.
RISK_PER_TRADE = 0.01

# INITIAL_BALANCE: стартовый баланс для paper trading (USDT).
INITIAL_BALANCE = 100.0

# LEVERAGE: кредитное плечо. 10 = позиция в 10 раз больше залога.
#   Больше плечо = больше прибыль И убыток.
LEVERAGE = 5

# MOVE_TO_BREAKEVEN_PCT: при какой прибыли (%) переносим стоп
#   в безубыток. 0.5 = когда сделка в плюсе на 0.5%.
MOVE_TO_BREAKEVEN_PCT = 0.5

# ── Интервалы обновления ─────────────────────
# Как часто бот проверяет данные (в секундах).
UPDATE_INTERVAL = 5

# ── Логирование ──────────────────────────────
LOG_DIR = "logs"                      # папка для логов
TRADE_LOG_FILE = "trades.json"        # файл со сделками
TRADE_CSV_FILE = "trades.csv"         # CSV для Excel/Google Sheets

# ── BTC-корреляция ───────────────────────────
# Если BTC резко падает, альты обычно падают сильнее.
# BTC_CORRELATION_CHECK: включить проверку связи с BTC.
BTC_CORRELATION_CHECK = True

# BTC_DUMP_THRESHOLD: если BTC упал на столько % за час,
#   не открывать лонги на альтах.
BTC_DUMP_THRESHOLD = -1.0  # -1% за час

# BTC_PUMP_THRESHOLD: если BTC вырос на столько % за час,
#   не открывать шорты на альтах.
BTC_PUMP_THRESHOLD = 1.0   # +1% за час
